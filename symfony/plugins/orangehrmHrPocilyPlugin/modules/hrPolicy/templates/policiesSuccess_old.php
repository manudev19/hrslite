        <h4><b>3.  Employment</b></h4>        
        <h5><b>3.1 Appointment</b></h5>
        <p>Your appointment is based on your performance in the recruitment process of our Company. Your appointment is subject to the Terms and Conditions mentioned in the Appointment Letter given to you. You need to give your acceptance by signing and returning the duplicate copy of the Appointment Letter to the HR department. Your signing the Appointment Letter implies that you agree to the Terms and Conditions mentioned in the Appointment Letter.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.1. Confidentiality</b></h5></p> 
        <p style="padding-left: 10px">During the term of your employment with the company, you will have access to and will get acquainted with various trade secrets of our company and/or our company’s client. You agree that you shall not misuse, misappropriate or disclose any of the trade secrets or confidential information of any description either directly or indirectly or use them in any way either during the course of the present employment or anytime thereafter, except as required in the course of your employment with the company.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.2. Non-Solicitation</b></h5></p>
        <p style="padding-left: 10px">You agree that during the period of the employment and for one year thereafter, you shall not solicit or accept business or employment from Sun Technology Integrators Pvt. Ltd. clients or any subsidiary or affiliate, or engage in similar occupation or profession at a facility operated by the client or any subsidiary or affiliate directly or through any other company.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.3. Compliance with Laws/Rules</b></h5></p>
        <p style="padding-left: 10px">You will keep yourself fully acquainted with the various laws, orders, rules, regulations, directives etc. and the notification therein from time to time affecting or concerning directly or indirectly the company or its business and affairs and in attending to the various duties assigned to you, from time to time.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.4. Notice Period</b></h5></p>
        <p style="padding-left: 10px">If you desire to leave employment of Sun Technology Integrators Pvt. Ltd, you agree to give at least 2 months written notice to Sun Technology Integrators Pvt. Ltd. Should this Agreement be terminated without cause by Sun Technology Integrators Pvt. Ltd., then you shall be afforded a 2 months’ notice of said termination.  In case of adversaries, Sun Technology Integrators Pvt. Ltd. has full rights to take any necessary actions. Such notice shall be given to you either verbally or in writing and if in writing shall be effective as of date delivered, or sent, if by mail   to your last known address carried on Sun Technology Integrators Pvt. Ltd. files.</p>

        <p style="padding-left: 10px">During the probation period, your services are liable to be terminated by the Company without assigning any reason and without any notice period. However, during the probation period, you will be required to give two months notice at the time of your resignation.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.5. Other Employment/Work</b></h5></p>
        <p style="padding-left: 10px">Your position is of whole time employee of the company and you shall devote your whole time, attention and abilities during hours of work to your duties exclusively to the business of the company. You will not take up any other work under any circumstances whether directly or indirectly any other job including work voluntary in nature for remuneration (part time or otherwise) or carry on a business of whatever kind or work in advisory capacity or be interested directly or indirectly in any other trade or business during the employment with the company without permission from the management.</p></br>
  
        <p><h5 style="padding-left: 10px"><b>3.1.6. Job Description and Duties</b></h5></p>
        <p style="padding-left: 10px">At the time of joining, the employee will be given with a set of Job description that he/she needs to perform at the Job. In addition, according to the employee position in the Organization and the requirements of the company, the employee may be given a new set of Job description discussed mutually by the employee and the supervisor. Signed Job description sheet has to be submitted to the HR Department which will be placed in the Employee's Permanent personnel file and will be used for future appraisals.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.7. Roles and Responsibilities</b></h5></p> 
        <p style="padding-left: 10px">The immediate superior of the employee will specify the Roles and Responsibilities of the employee at the time of joining. Apart from the roles mentioned initially, the Company may assign new Roles and Responsibilities to the employee as and when required.</p></br>
        
        <p><h5 style="padding-left: 10px"><b>3.1.8. Shift, hours of work, attendance and weekly off</b></h5></p>
        <p style="padding-left: 10px">Normally company run into 9 hours shifts including lunch/dinner/snacks break and you can be put into any of the shift timings as per the company’s requirement and your shift timings can be changed without any prior notice. You are required to adhere the time schedule and be punctual about the office timing. However you shall work as required by the company from time to time. Your shift/working/duty hours, attendance and weekly off shall be regulated to suit the company’s requirements from time to time. You may also be required to attend duties on holidays/weekly off days as per exigencies of work. You agree to fill out time sheet on a weekly basis and Job related form on daily basis.</p></br>
  
        <p><h5 style="padding-left: 10px"><b>3.1.9. Supervision/Control</b></h5></p>
        <p style="padding-left: 10px">You shall work under the supervision and control of such person/persons as decided by the company from time to time. You shall most diligently and faithfully carryout instructions or discharge the duties given to you by your superiors or person whom you are placed to work in the overall interest of the company.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.10. Leave and Holidays</b></h5></p>
        <p style="padding-left: 10px">You are entitled to an annual holiday of 28 days, comprising of 10 holidays and 18 Paid Vacation, inclusive of casual and sick leave in each year. The leave year runs from 1st January to 31st December. The holidays will be fixed or rotated within a team at the discretion of the Manager and the Client needs.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.11. Notification of Sickness and other absence</b></h5></p>
        <p style="padding-left: 10px">If you are absent from work for any reason and if your absence has not previously been authorized by the company, you must inform your immediate Manager.</p>

        <p style="padding-left: 10px">Your unauthorized absence must be properly explained and in the case of absence of uncertain duration, you must keep the company informed on a daily basis.</p>

        <p style="padding-left: 10px">If you are absent from work due to sickness or injury which continues for more than three days, you must provide the company with a medical certificate on or before 4th day of sickness or injury and thereafter medical certificate must be provided to the company to cover any continued absence.   </p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.12. Company’s property</b></h5></p>
        <p style="padding-left: 10px">You will be responsible for safe keeping and return in good condition and order of all the company’s property, equipment’s, instruments, tools, books etc. which may be given to you for your use, custody, care or charge. The company have the right to deduct the money value of all such things from your dues and take such other action as the company may due proper in the event of your failure to account for such property to company’s satisfaction.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.13. Termination of Employment</b></h5></p>
        <p style="padding-left: 10px">If you desire to leave employment of Sun Technology Integrators Pvt. Ltd, you agree to give at least 2 months written notice to Sun Technology Integrators Pvt. Ltd. Should this Agreement be terminated without cause by Sun Technology Integrators Pvt. Ltd., then you shall be afforded a 2 months notice of said termination.  In case of adversaries, Sun Technology Integrators Pvt. Ltd. has full rights to take any necessary actions. Such notice shall be given to you either verbally or in writing and if in writing shall be effective as of date delivered, or sent, if by mail   to your last known address carried on Sun Technology Integrators Pvt. Ltd. files.  </p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.14.  Violation of Terms and Conditions</b></h5></p>
        <p style="padding-left: 10px">If you violate the Terms and Conditions of employment laid out in this letter Sun Technology Integrators Pvt. Ltd shall be entitled to take legal action against you.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.15. Probationary Period</b></h5></p>
        <p style="padding-left: 10px">The probationary period serves as an extension of the selection process and gives both you and your supervisor time to see if the job is right for you, and you are right for the job. As per the Company standards you will be on probation for a period of 6 months from the date of joining and may be confirmed as a permanent employee upon successful completion of your probation. Your performance during this period will be appraised based on certain standards set by the company, which will be discussed with you at the time of joining. In addition to your performance during the Probationary Period, your confirmation as a permanent employee is subject to your submitting the requisite documents as required by the Company. If you demonstrate satisfactory work progress during the Probationary period and the decision is made to continue your employment at the end of the probationary period, you will receive a Confirmation Letter in this regard. If work progress is not satisfactory, an employee can be terminated with an opportunity for appeal at the management's discretion.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.16. Work Schedule </b></h5></p>
        <p style="padding-left: 10px">The standard workweek schedule is five days per week, eight hours a day plus a one-hour meal period. Other schedules apply to part-time and some shift employees. The employee and the supervisor shall agree upon in advance of Variable Work Schedule to be followed, consistent with the needs of the Company.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.17. Work Behavior</b></h5></p>
        <p style="padding-left: 10px">It is essential that all employees accept personal responsibility for maintaining high standards of conduct and job performance, including observance of the Company rules and policies. Violations of these standards will result in disciplinary action. Disciplinary action is considered a dimension of performance evaluation. It is a corrective process to help employees overcome work-related shortcomings, strengthen work performance and achieve success.</p></br>

        <p><h5 style="padding-left: 10px"><b>3.1.18. Transfers</b></h5></p>
        <p style="padding-left: 10px">You are also liable to be transferred to any of the new Offices that the Company might open or you may at times need to for on some of the Company assignments abroad. Transfers may involve a promotion, reassignment, demotion or lateral move. Your leave credits and all the other benefits go with you. Your personel File is also transferred to your new Office.</p> 
        </br>         
              
        <h5><b>3.2 Joining Formalities</b></h5>
        <p>At the time of joining, you will be given with a New Employee Joining Application, which you need to fill in completely and submit it to the HR Department along with the following.</p>
        <ol style="list-style-type: decimal;list-style-position: inside;padding-left: 10px">
          <li>3 passport size photographs</li>
          <li>Experience Certificates (in case you are already employed) </li>
          <li>Qualification Certificates</li>
          <li>Proof of Identity & Address</li>
          <li>Emergency contact name, number and address</li>
          <li>Permanent address and contact details</li>
        </ol>
        <p>All the above documents will be kept in Employee’s Permanent personnel File along with the New Employee Joining Form. Employees will be issued an ID card within 8 working days of their appointment and their Biometric access will be enabled.</p>        
        </br>
        <h5><b>3.3 Personnel Files</b></h5>       
        <p>Sun Technologies maintains a personnel file on each employee. The personnel file includes such information as the employee’s job application, resume, record of training, documentation of performance appraisal and salary increases, and other employment record.</p> 
        <p>Personnel files are the property of Sun Technologies, and access to the information they contain is restricted. Generally, only HUMAN RESOURCE personnel will have access to these files.</p></br>    
              
        <h5><b>3.4 Personal Data Change</b></h5>
        <p>It is the responsibility of each employee to promptly notify HR Department of any changes in personal data. Personal mailing addresses, telephone numbers, numbers and name of the dependents, individuals to be contacted in the event of emergency, educational accomplishments, and other such status reports should be accurate and current at all times. If any personal data has changed, notify the [HUMAN RESOURCE DEPARTMENT].</p></br>
              
        <h5><b>3.5 Performance Management</b></h5>
        <p>Performance appraisal is done by immediate supervisor and they have access to all the data required during complete appraisal process. At the time of joining you will be given a copy of your Job Description along with a set of expectations that the Company is looking in you.</p> </br>

        <p ><h5><b style="padding-left: 10px">3.5.1 Performance Appraisal</b></h5></p>
        <p style="padding-left: 10px">Performance Evaluation will be done once in a year. Self-Appraisal forms will be given to employees to appraise themselves on what their accomplishments were during the Appraisal Year. Completed forms have to be returned, through appropriate channels, to the HR Team. Upon receipt of the evaluation form, the following actions will be accomplished: </p>
        <p style="padding-left: 10px">The Immediate superior will:</p> 

        <ol style="list-style-type: decimal;list-style-position: outside;padding-left: 25px">
        <li>Complete the evaluation form as promptly as possible. Exceptional must be discussed in Comments section of the evaluation form. Describe why performance is not satisfactory and specify how performance can be improved, or explain why performance is outstanding.</li>
        <li>Discuss evaluation with the employee emphasizing strong and weak points in job performance. Commend the employee for a job well done if applicable and discuss specific corrective action if warranted. Set mutual goals for the employee to reach before the next performance evaluation. Recommendations should specifically state methods to correct weakness and/or prepare the employee for future promotions.</li>

        <li>Allow the employee to make any written comments he/she desires. Have employee sign the evaluation form and initial after supervisor's comments.</li>

        <li>Forward the original copy of the evaluation form to the HR department. Retain a copy of the complete form for the department and the employee.</li>

        <li>Subsequent to the completion of this evaluation by the supervisor, and review by the employee, revisions must be discussed by both parties. </li>
        </ol>

        <p style="padding-left: 10px">The CEO will as and when require:</p> 
        <ol style="list-style-type: decimal;list-style-position: inside;padding-left: 15px">
          <li>Review forms to ensure further actions on the appraisal.</li>
          <li>See that proper actions have been taken to resolve any identify conflicts</li>
          <li>Account for all evaluation forms in his area of responsibility.</li>
          <li>Forward all original forms together as a group to the HR Department.</li>
          <li>Company values and personal development of employees, are the two pointers which is given highest importance by CEO.</li>
        </ol>
        <p style="padding-left: 10px">HR Team will:</p>
        <p style="padding-left: 10px">Review for completeness and accuracy. Any unresolved problems will be brought to the attention of the consumed line of authority. The completed form will be placed in the employee's permanent Personnel File.</p>

        <p><h5><b style="padding-left: 10px">3.5.2 Promotions</b></h5></p> 
        <p style="padding-left: 10px">Based on your Performance Appraisals, accordingly you might be promoted to a senior position as decided by the Company.</p> 
