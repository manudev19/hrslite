        </br>       

        <h5><b>5.4 Leaves </b></h5>
        <p>You are entitled to 18 days of leaves on a prorated basis. You will be eligible for 1.5 days every month. The unused leaves will be carried forward to the next year or can be encashed on the basic pay. The same has to be notified by the employees to the HR at the end of the December every year. Maximum of 15 days can be carry forwarded to next year, if it exceeds 15 days then it will be encashed on the basic pay.</p>
        </br>

        <h5><b>5.5 Maternity Leave</b></h5>
        <p>Women employed in our Company are eligible to claim maternity benefits.</p>
        <p>The employee should have worked in the company for at least 80 days in the last 12 months and they can avail leave for 6 months.</p>
        <p>Leaves can be availed for 8 weeks prior to childbirth and 18 weeks post the childbirth.
        Employees are entitled to get paid while they are on maternity leave.</p>
